const metadataById = new Map(adminWardMetadata.map((ward) => [ward.id, ward]));

function pointInRing([lng, lat], ring) {
    let inside = false;

    for (let i = 0, j = ring.length - 1; i < ring.length; j = i, i += 1) {
        const [xi, yi] = ring[i];
        const [xj, yj] = ring[j];
        const intersects = ((yi > lat) !== (yj > lat))
            && (lng < ((xj - xi) * (lat - yi)) / ((yj - yi) || Number.EPSILON) + xi);

        if (intersects) {
            inside = !inside;
        }
    }

    return inside;
}

function pointInPolygon([lng, lat], rings) {
    if (!rings?.length || !pointInRing([lng, lat], rings[0])) {
        return false;
    }

    for (let i = 1; i < rings.length; i += 1) {
        if (pointInRing([lng, lat], rings[i])) {
            return false;
        }
    }

    return true;
}

export function pointInGeometry(point, geometry) {
    if (!geometry) {
        return false;
    }

    if (geometry.type === 'Polygon') {
        return pointInPolygon(point, geometry.coordinates);
    }

    if (geometry.type === 'MultiPolygon') {
        return geometry.coordinates.some((polygon) => pointInPolygon(point, polygon));
    }

    return false;
}

export function extractAdminWardId(value) {
    const match = /Admin Ward\s*(\d+)/i.exec(value || '');
    return match ? Number(match[1]) : null;
}

export function buildAdminWard(feature) {
    const id = extractAdminWardId(feature?.properties?.name);
    const metadata = id ? metadataById.get(id) : null;

    if (!metadata) {
        return null;
    }

    return {
        ...metadata,
        boundaryName: feature.properties.name,
    };
}

export function findAdminWardFeature(lat, lng, geoJson) {
    return geoJson?.features?.find((feature) => pointInGeometry([lng, lat], feature.geometry)) || null;
}

export function findAdminWardFromGeoJson(lat, lng, geoJson) {
    const feature = findAdminWardFeature(lat, lng, geoJson);
    return feature ? buildAdminWard(feature) : null;
}
